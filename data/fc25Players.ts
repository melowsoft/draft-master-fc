// Helper function to search players
export function searchFC25Players(query: string, filters?: {
  position?: Position;
  league?: League;
  minRating?: number;
  maxRating?: number;
}): FC25Player[] {
  let results = fc25Players;
  
  if (query && query.length >= 2) {
    const q = query.toLowerCase();
    results = results.filter(p => 
      p.name.toLowerCase().includes(q) ||
      p.fullName?.toLowerCase().includes(q) ||
      p.club?.toLowerCase().includes(q) ||
      p.nationality.toLowerCase().includes(q)
    );
  }
  
  if (filters?.position) {
    results = results.filter(p => 
      p.position === filters.position || p.positions.includes(filters.position!)
    );
  }
  
  if (filters?.league) {
    results = results.filter(p => p.league === filters.league);
  }
  
  if (filters?.minRating) {
    results = results.filter(p => p.rating >= filters.minRating!);
  }
  
  if (filters?.maxRating) {
    results = results.filter(p => p.rating <= filters.maxRating!);
  }
  
  return results.sort((a, b) => b.rating - a.rating);
}

// Get top players by position
export function getTopPlayersByPosition(position: Position, limit = 10): FC25Player[] {
  return fc25Players
    .filter(p => p.position === position || p.positions.includes(position))
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
}

// Get all unique clubs
export function getFC25Clubs(): string[] {
  return [...new Set(fc25Players.map(p => p.club).filter((c): c is string => !!c))].sort();
}

// Get all unique nationalities  
export function getFC25Nationalities(): string[] {
  return [...new Set(fc25Players.map(p => p.nationality))].sort();
}
